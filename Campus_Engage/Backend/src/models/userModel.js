import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
    fullName: {
        type: String,
        required: true,
        trim: true
    },
    userName: {
        type: String,
        required: true,
        trim: true, index: true, unique: true, lowercase: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        lowercase: true,
        trim: true,
    },
    avatar: {
        type: String,
        required:true
    },
    password: {
        type: String,
        required: [true,"Password is required!"]
    },
    refreshToken: { type: String }

},{timestamps:true});

// 1. Encrypt Password before saving
